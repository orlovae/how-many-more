public class CoordinateText {
    private float x;
    private float y;

    CoordinateText() {
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    void setX(float x) {
        this.x = x;
    }

    void setY(float y) {
        this.y = y;
    }
}
